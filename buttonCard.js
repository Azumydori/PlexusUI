//LONDON

function changeLondon() {

    var divchange = document.getElementById('cardLondon');
    if (divchange.style.height == '') {
        divchange.style.height = '32rem'
    } else {
        divchange.style.height = ''
    }

    var buttonchange = document.getElementById('buttonLondon');
    if (buttonchange.style.transform == '') {
        buttonchange.style.transform = 'rotate(180deg)'
        buttonchange.style.height 
    } else {
        buttonchange.style.transform = ''
    }

    var buttonheight = document.getElementById('buttonLondon');
    if (buttonheight.style.marginTop == '') {
        buttonheight.style.marginTop = '8rem'
    } else {
        buttonheight.style.marginTop = ''
    }

    var textappear = document.getElementById('textLondon');
    if (textappear.style.overflow == '') {
        textappear.style.overflow = 'visible'
    } else {   
        textappear.style.overflow = ''
    }

};

